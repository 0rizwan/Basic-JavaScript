// Date Methods
var a = new Date();
// 1)toDateString()- It shows the full date with month and year.
console.log("full date- ",a.toDateString());

// 2) getDate()- It shows the date.
console.log("Date- ",a.getDate());   

// 3) getDay()- It shows the day of the week.
console.log("Day- ",a.getDay());

// 4) getFullYear()- It shows the year. 
console.log("Year- ",a.getFullYear());

// 5)getMonth()- It shows the Month. 
console.log("Month- ",a.getMonth());  

console.log("Time: " + a.getHours(), a.getMinutes(), a.getSeconds(), a.getMilliseconds());
